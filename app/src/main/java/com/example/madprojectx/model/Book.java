package com.example.madprojectx.model;

public class Book {
    public String bookHostName;
    public String bookHostID;
    public String bookBookerID;
    public String bookOwnerID;
    public String bookPhone;
    public String bookFirstName;
    public String bookLastName;

    public Book() {
    }

    public String getBookHostID() {
        return bookHostID;
    }

    public void setBookHostID(String bookHostID) {
        this.bookHostID = bookHostID;
    }

    public String getBookHostName() {
        return bookHostName;
    }

    public void setBookHostName(String bookHostName) {
        this.bookHostName = bookHostName;
    }

    public String getBookBookerID() {
        return bookBookerID;
    }

    public void setBookBookerID(String bookBookerID) {
        this.bookBookerID = bookBookerID;
    }

    public String getBookOwnerID() {
        return bookOwnerID;
    }

    public void setBookOwnerID(String bookOwnerID) {
        this.bookOwnerID = bookOwnerID;
    }

    public String getBookPhone() {
        return bookPhone;
    }

    public void setBookPhone(String bookPhone) {
        this.bookPhone = bookPhone;
    }

    public String getBookFirstName() {
        return bookFirstName;
    }

    public void setBookFirstName(String bookFirstName) {
        this.bookFirstName = bookFirstName;
    }

    public String getBookLastName() {
        return bookLastName;
    }

    public void setBookLastName(String bookLastName) {
        this.bookLastName = bookLastName;
    }
}


